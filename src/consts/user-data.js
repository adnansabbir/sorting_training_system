const getRandomNumbers = (size, minNumber, maxNumber) => {
    if (maxNumber - minNumber < size) throw new Error(`Difference of min max should be greater than ${size}`);
    const unique = new Set();
    while (unique.size < size) {
        unique.add(Math.floor(Math.random() * (maxNumber - minNumber + 1) + minNumber));
    }
    return Array.from(unique);
}

export const getUserData = (size) => {
    if (size > 100 || size < 0) throw new Error('Size must be within 20 to 100');

    const randomIndexes = getRandomNumbers(size, 0, 99);
    const randomNumber = getRandomNumbers(size, 0, 100);
    return randomIndexes.map((randIdx, i) => ({...UserData[randIdx], potatoes: randomNumber[i]}));
};
